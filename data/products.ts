export const featuredProducts = products.filter((product) => product.isPopular).slice(0, 8);
export const deliFavorites = products.filter((product) => product.isDeliMade || product.category === "Deli").slice(0, 4);
export const dealProducts = products.filter((product) => product.isDiscounted);
export const freshProducts = products.filter((product) => product.isFresh);
export const localProducts = products.filter((product) => product.isLocal);

export function getProductBySlug(slug: string) {
  return products.find((product) => product.slug === slug);
}

export function getProductsByCategory(category: string) {
  return products.filter((product) => product.category === category);
}

export function getRelatedProducts(product: Product, limit = 4) {
  return products
    .filter((entry) => entry.id !== product.id && (entry.category === product.category || entry.subcategory === product.subcategory))
    .slice(0, limit);
}
